// scripts/generate-update-manifest.mjs
//
// Build a `latest.json` next to the MSI/NSIS bundles produced by
// `pnpm tauri build`. The Tauri auto-updater plugin polls a URL that
// returns this manifest; given the project is local-only by default,
// the URL can point at a `file://…/latest.json` on disk during dev.
//
// Inputs:
//   --version   <semver>     Required. The version this build advertises.
//   --bundle    <dir>        Default: ./src-tauri/target/release/bundle
//   --notes     <text>       Optional release notes body.
//   --notes-file <path>      Optional: read notes from a markdown file.
//   --download-base <url>    Optional: prefix to use for the bundle URLs.
//                            Defaults to a `file://` URL pointing at the
//                            local bundle file (suitable for local-only).
//
// Side effects:
//   - Reads `<bundle>/{msi,nsis}/*.{msi,exe}` and the matching `.sig`
//     files generated by Tauri when `bundle.createUpdaterArtifacts` is
//     true and a private signing key is present in the environment.
//   - Writes `<bundle>/latest.json`.
//
// The script does NOT sign anything itself — signing is Tauri's job
// during the bundle step. If the `.sig` file is missing for a bundle we
// emit a clear error rather than producing a manifest the updater
// would reject silently.

import { readFile, readdir, stat, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import { resolve, join, basename } from "node:path";
import { pathToFileURL } from "node:url";

function parseArgs(argv) {
  const out = {};
  for (let i = 2; i < argv.length; i += 1) {
    const arg = argv[i];
    if (!arg.startsWith("--")) continue;
    const key = arg.slice(2);
    const value = argv[i + 1] && !argv[i + 1].startsWith("--") ? argv[i + 1] : "";
    out[key] = value;
    if (value) i += 1;
  }
  return out;
}

async function readBundleFile(filePath) {
  const sigPath = `${filePath}.sig`;
  if (!existsSync(sigPath)) {
    throw new Error(
      `Missing signature for ${basename(filePath)}.\n` +
        `Expected: ${sigPath}\n` +
        `Did the build run with TAURI_SIGNING_PRIVATE_KEY set? See docs/auto_updater_setup.md.`,
    );
  }
  const signature = (await readFile(sigPath, "utf8")).trim();
  return { signature };
}

async function findInstaller(dir, extensions) {
  if (!existsSync(dir)) return null;
  const entries = await readdir(dir);
  for (const entry of entries) {
    const lower = entry.toLowerCase();
    if (extensions.some((ext) => lower.endsWith(ext))) {
      const full = join(dir, entry);
      const s = await stat(full);
      if (s.isFile()) return full;
    }
  }
  return null;
}

function bundleUrl(downloadBase, filePath) {
  if (!downloadBase) return pathToFileURL(filePath).href;
  // Trim trailing slash from base and produce a clean URL with the bundle filename.
  const base = downloadBase.replace(/\/+$/, "");
  return `${base}/${basename(filePath)}`;
}

async function main() {
  const args = parseArgs(process.argv);
  const version = args.version;
  if (!version) {
    console.error("error: --version is required");
    process.exit(1);
  }
  const bundleRoot = resolve(args.bundle || "src-tauri/target/release/bundle");
  const downloadBase = args["download-base"] || "";

  let notes = args.notes || "";
  if (!notes && args["notes-file"]) {
    notes = (await readFile(args["notes-file"], "utf8")).trim();
  }

  // Tauri's NSIS path is the canonical updater target on Windows because
  // it supports the in-place upgrade flow `installMode: "passive"` needs.
  // MSI is included alongside for users who still want it, but NSIS is
  // the entry written into the manifest.
  const nsisDir = join(bundleRoot, "nsis");
  const nsisInstaller = await findInstaller(nsisDir, [".exe"]);
  if (!nsisInstaller) {
    console.error(
      `error: no NSIS installer found under ${nsisDir}.\n` +
        `Run \`pnpm tauri build\` first; ensure tauri.conf.json's bundle.targets includes "nsis".`,
    );
    process.exit(1);
  }

  const { signature } = await readBundleFile(nsisInstaller);
  const url = bundleUrl(downloadBase, nsisInstaller);

  const manifest = {
    version,
    notes,
    pub_date: new Date().toISOString(),
    platforms: {
      "windows-x86_64": {
        signature,
        url,
      },
    },
  };

  const out = join(bundleRoot, "latest.json");
  await writeFile(out, `${JSON.stringify(manifest, null, 2)}\n`, "utf8");

  console.log(`wrote ${out}`);
  console.log(`  version=${version}`);
  console.log(`  url=${url}`);
  console.log(`  signed=true`);
}

main().catch((err) => {
  console.error(err.message ?? err);
  process.exit(1);
});
